#for
#menggunakan list
angka_list = [0,1,2,3,4]
print(angka_list)

for i in angka_list:
    print("i sekarang adalah : ",i)
print("SELESAI\n")

#menggunakan range
for i in range(5):
    print("i sekarang adalah : ",i)
print("selesai\n")

for j in range(1,5):
    print("j sekarang adalah : ",j)
print("selesai\n")

for k in range(1,10,2):
    print("k sekarang adalah : ",k)
print("SELESAI\n")

#menggunakan tuple
buah_tuple = ("apel", "anggur", "jeruk", "mangga")

for i in buah_tuple:
    print("buah :",i)
print("SELESAI\n")

#menggunakan string
kata_string = "indonesia"

for i in kata_string:
    print(i)
print("SELESAI\n")

for j in "indonesia":
    print(j)
print("SELESAI")

